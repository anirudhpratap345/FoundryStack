import { NextRequest, NextResponse } from 'next/server';

export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    
    // Forward the request to the Writer Agent
    const response = await fetch('http://localhost:8003/write', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(body),
    });

    if (!response.ok) {
      throw new Error(`Writer Agent error: ${response.status} ${response.statusText}`);
    }

    const data = await response.json();
    return NextResponse.json(data);
  } catch (error) {
    console.error('Writer Agent proxy error:', error);
    return NextResponse.json(
      { error: 'Failed to generate content with Writer Agent' },
      { status: 500 }
    );
  }
}
